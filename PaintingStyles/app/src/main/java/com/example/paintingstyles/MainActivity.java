    // Вспомогательный метод для добавления одного стиля в макет
    private void addPaintingStyle(String name, String description) {
        // TextView для названия стиля
        TextView nameTextView = new TextView(this);
        nameTextView.setText(name);
        nameTextView.setTextSize(TypedValue.COMPLEX_UNIT_SP, 22); // Размер текста
        nameTextView.setTypeface(null, Typeface.BOLD); // Жирный шрифт
        nameTextView.setTextColor(getResources().getColor(android.R.color.black)); // Черный цвет текста
        // nameTextView.setGravity(Gravity.CENTER_HORIZONTAL); // Центрирование
        LinearLayout.LayoutParams nameParams = new LinearLayout.LayoutParams(
                LinearLayout.LayoutParams.MATCH_PARENT,
                LinearLayout.LayoutParams.WRAP_CONTENT);
        nameParams.setMargins(0, dpToPx(16), 0, dpToPx(4)); // Отступы сверху 16dp, снизу 4dp
        nameTextView.setLayoutParams(nameParams);
        contentLayout.addView(nameTextView); // Добавляем в основной контейнер

        // TextView для описания стиля
        TextView descriptionTextView = new TextView(this);
        descriptionTextView.setText(description);
        descriptionTextView.setTextSize(TypedValue.COMPLEX_UNIT_SP, 16); // Размер текста
        descriptionTextView.setTextColor(getResources().getColor(android.R.color.darker_gray)); // Темно-серый цвет
        descriptionTextView.setLineSpacing(TypedValue.applyDimension(TypedValue.COMPLEX_UNIT_DIP, 4, getResources().getDisplayMetrics()), 1.0f); // Межстрочный интервал
        LinearLayout.LayoutParams descParams = new LinearLayout.LayoutParams(
                LinearLayout.LayoutParams.MATCH_PARENT,
                LinearLayout.LayoutParams.WRAP_CONTENT);
        descParams.setMargins(0, 0, 0, dpToPx(24)); // Отступ снизу 24dp для разделения стилей
        descriptionTextView.setLayoutParams(descParams);
        contentLayout.addView(descriptionTextView); // Добавляем в основной контейнер
    }

    // Вспомогательный метод для конвертации dp в px
    private int dpToPx(int dp) {
        return (int) TypedValue.applyDimension(
                TypedValue.COMPLEX_UNIT_DIP,
                dp,
                getResources().getDisplayMetrics()
        );
    }
